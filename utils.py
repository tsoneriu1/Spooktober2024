def print_ghost(ghost):
    match ghost:
        case "1": #classic ghost
            print("""
   ____
  ( o  o)
)~/    |~( 
 /    /
//////
       """)
        case "2": #obvious ghost
            print(""" 
    .-=,.
    (⊙_☉)
    (   )
    (  )
    (/)
""")
        case "3":#sneaky ghost
            print("""
    ~
  /  \\
 /     \\
( ⚆ _ ⚆ )
 \\\\\\\\
  /////
  \\\\
   ///
   \\
   /               
""")
        case "4": #elusive ghost
            print("""
            ,---.
            ( ｡･ ･｡)
        ~~~;.....:~~
            ,....;
            ,... `
        ` ..  .
        .  .
            
                  """)
        case "5": #agressive ghost
            print("""
                   .--.
                  (◣_◢ )
                <~~.  .~~>
                  (    )
                  ////\\
                  """)
